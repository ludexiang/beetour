package com.beetour.domain.item;

public class Picture {
	
	private String subSpic;     //小图地址
	private String subPic;		//缩略图地址
	private String subTpic;		//大图

	public String getSubSpic() {
		return subSpic;
	}

	public void setSubSpic(String subSpic) {
		this.subSpic = subSpic;
	}

	public String getSubPic() {
		return subPic;
	}

	public void setSubPic(String subPic) {
		this.subPic = subPic;
	}

	public String getSubTpic() {
		return subTpic;
	}

	public void setSubTpic(String subTpic) {
		this.subTpic = subTpic;
	}

}
